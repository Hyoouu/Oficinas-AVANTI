#PROGRAMA PARA TIRAR MÉDIA
#notas
nota1 = float(input("Digite a primeira nota: "))
nota2 = float(input("Digite a segunda nota: "))
nota3 = float(input("Digite a terceira nota: "))
#calcula a media
media = (nota1 + nota2 + nota3) / 3
#exibe a media
print("a média das 3 notas é: ", media)

#SEGUNDOS EM HORAS
horas = float(input("Digite o número de horas: "))
#converter horas em segundos
segundos = horas * 3600
#exibir resultado
print("Total de segundos ", segundos)

#QUADRADO DE DOIS NÚMEROS
num1 = float(input("Digite o primeiro número: "))
num2 = float(input("Digite o segundo número: "))
#calcular o quadrado dos números
quadrado1 = num1 ** 2
quadrado2 = num2 ** 2
#exibir resultados
print(f"resultado de {num1} é: {quadrado1}")
print(f"resultado de {num2} é: {quadrado2}")

